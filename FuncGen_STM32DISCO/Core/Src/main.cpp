void button_check(void)
{
    static uint8_t state = IDLE;
    switch(state)
    {
    case IDLE:
        if(flag_callback_menu==ON)
        {
            state = CHECK;
            counter = 0;
            flag_callback_menu = 0;
        }
        break;
    case CHECK:
        if(counter >= DEBOUNCINGTIME)
        {
            if(pin_state == HAL_GPIO_ReadPin(GPIOD, button_callback))
            {
                if(pin_state == GPIO_PIN_SET)
                    state=PUSH;
                else
                    state=NPUSH;
            }
            else
                state = IDLE;
        }
        break;
    case PUSH:
        pressed_button = 1;
        button_action(button_callback);
        state = IDLE;
        break;
    case NPUSH:
        pressed_button = 0;
        state = IDLE;
        break;
    }
}


